function parseClients(raw: string, startId: number): Client[] {
  return raw.trim().split('\n').map((line, i) => {
    const [nome, comp, juros, boleto, pix, regional, executivo, dias, parcelas, situacao] = line.split('|');
    const jurosNum = parseFloat(juros);
    const diasNum = parseInt(dias);
    const sit = situacao.trim() as Client['situacao'];
    
    const flags: Client['flags'] = [];
    if (jurosNum > 0) flags.push('Juros');
    if (diasNum > 90) flags.push('Prioridade');
    if (sit === 'PARCELADO') flags.push('Parcelamento');
    if (sit === 'DISTRATO') flags.push('Jurídico');

    return {
      id: String(startId + i),
      nome: nome.trim(),
      cnpj: '',
      regional: regional.trim(),
      executivo: executivo.trim(),
      compensacao: parseFloat(comp),
      juros: jurosNum,
      boletoVitbank: parseFloat(boleto),
      pixMonetali: parseFloat(pix),
      diasAtraso: diasNum,
      parcelas: parseInt(parcelas),
      situacao: sit,
      flags,
    };
  });
}

